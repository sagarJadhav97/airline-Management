    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     try{
        
      pst = con.prepareStatement("SELECT `payment_amount`, `ticket_no`, `pnr`, `payment_type` FROM `pay_details` WHERE pnr='"+ppnnn.getText()+"'");
         rs = pst.executeQuery();
          
           if(rs.next()==false){
            JOptionPane.showMessageDialog(this, "Recorde Not found");
            }else{
            String fno = String.valueOf(rs.getString("ticket_no"));
            String price = String.valueOf(rs.getString("payment_amount"));
           
            jTextField3.setText(fno);   
            amt.setText(price);
            charges.setText("350");
          }
      
    }catch(HeadlessException | SQLException ex){
      JOptionPane.showMessageDialog(this,ex.getMessage());
      }
    }//GEN-LAST:event_jButton1ActionPerformed

